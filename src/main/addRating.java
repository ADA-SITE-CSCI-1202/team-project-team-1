    private void addValue(String value){
	String path = "C:\\Task\\personalDB.xlsx";
	try {
	    FileInputStream fileIn = new FileInputStream(path);
	    BufferedInputStream excelBuf = new BufferedInputStream(fileIn);
	    XSSFWorkbook book = new XSSFWorkbook(excelBuf);
	    XSSFSheet sheet = book.getSheetAt(0);
	    XSSFRow row = sheet.getRow(Main.row + 1);
	    XSSFCell cell = row.getCell(Main.column);
	    if(cell == null){
		cell = row.createCell(Main.column);
		}
	    cell.setCellValue(value);

	    FileOutputStream fos = new FileOutputStream(path);
	    book.write(fos);

	    fos.close();
	    book.close();
	} catch (FileNotFoundException ex) {
	    Logger.getLogger(addRating.class.getName()).log(Level.SEVERE, null, ex);
	} catch (IOException ex) {
	    Logger.getLogger(addRating.class.getName()).log(Level.SEVERE, null, ex);
	}
    }

